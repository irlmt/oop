    private void l5ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_l5ButtonActionPerformed
        if(null != l5List.getSelectedValue())switch (l5List.getSelectedValue().toString()) {
            case "1":
                l5TextField2.setText(LabFifth.task1(l5TextField1.getText()));
                l5TextField1.setText("");
                break;
            case "2":
                l5TextField2.setText(LabFifth.task2(l5TextField1.getText()));
                l5TextField1.setText("");
                break;
            case "3":
                l5TextField2.setText(LabFifth.task3(l5TextField1.getText()));
                l5TextField1.setText("");
                break;
            case "4":
                char c = l5TextField1.getText().charAt(0);
                l5TextField2.setText(LabFifth.task4(l5TextField1.getText().substring(2, l5TextField1.getText().length()), c));
                l5TextField1.setText("");
                break;
            case "5":
//                if(!l5TextField1.getText().isEmpty()){
                    try{
                        l5TextField2.setText(LabFifth.task5(l5TextField1.getText()));
                    }
                    catch(NoSuchElementException e){
                        l5TextField2.setText(e.getMessage());
                    }
                    l5TextField1.setText("");
                
                break;
            case "6":
                l5TextField2.setText(LabFifth.task6(l5TextField1.getText()));
                l5TextField1.setText("");
                break;
            case "7":
                l5TextField2.setText(LabFifth.task7(l5TextField1.getText()));
                l5TextField1.setText("");
                break;
            default:
                break;
        }
    }//GEN-LAST:event_l5ButtonActionPerformed

