    private void l1ChangeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_l1ChangeButtonActionPerformed
        String name = l1textField1.getText();
        String str = l1TextField2.getText();
        DefaultTableModel model = (DefaultTableModel) l1Table.getModel();
        for (int i = 0; i< l1Table.getRowCount(); i++){
            if(l1Table.getValueAt(i, 0).equals(name)){
                if (labFirst.changeStrategy(name, str)){
                    model.setValueAt(labFirst.getStrategy(name), i, 1);
                }
            }
        }
        l1TextField2.setText("");
    }//GEN-LAST:event_l1ChangeButtonActionPerformed

